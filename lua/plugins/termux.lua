  -- Mobile-optimized status line
  {
    "nvim-lualine/lualine.nvim",
    dependencies = { "nvim-tree/nvim-web-devicons" },
    config = function()
      local function is_termux()
        return vim.fn.has('termux') == 1
      end
      
      local function battery_status()
        if not is_termux() then return "" end
        local handle = io.popen("termux-battery-status 2>/dev/null")
        if not handle then return "" end
        local result = handle:read("*a")
        handle:close()
        
        if result and result ~= "" then
          local battery_data = vim.fn.json_decode(result)
          if battery_data and battery_data.percentage then
            local percentage = battery_data.percentage
            local icon = "üîã"
            if battery_data.status == "CHARGING" then
              icon = "‚ö°"
            elseif percentage < 20 then
              icon = "ü™´"
            elseif percentage < 50 then
              icon = "üîã"
            else
              icon = "üîã"
            end
            return string.format("%s %d%%", icon, percentage)
          end
        end
        return ""
      end
      
      local function memory_usage()
        if not is_termux() then return "" end
        local handle = io.popen("free -h | awk 'NR==2{printf \"%.1f/%.1fG\", $3*1024, $2*1024}'")
        if not handle then return "" end
        local result = handle:read("*a")
        handle:close()
        return result and result ~= "" and "üíæ " .. result or ""
      end
      
      require("lualine").setup({
        options = {
          icons_enabled = true,
          theme = "auto",
          component_separators = { left = "", right = "" },
          section_separators = { left = "", right = "" },
