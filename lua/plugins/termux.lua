      vim.notify = notify
      
      -- Termux-specific notifications
      if vim.fn.has('termux') == 1 then
        local original_notify = notify
        notify = function(msg, level, opts)
          -- Send to system notifications if available
          vim.fn.system(string.format('termux-notification --title "Neovim" --content "%s" 2>/dev/null', msg))
          return original_notify(msg, level, opts)
        end
        vim.notify = notify
      end
    end,
    keys = {
